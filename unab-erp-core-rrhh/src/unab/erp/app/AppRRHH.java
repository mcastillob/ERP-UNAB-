    public void cargaJtable(List<Empleado> empleados){
        
      
      
        
         String registros[] = new String[8];
         DefaultTableModel modelo = (DefaultTableModel) jTableEmpleados.getModel();
       

        
         for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
       
        for (Empleado emp : empleados) {
            registros[0] = emp.getRut();
            registros[1] = emp.getNombre();
            registros[2] = emp.getApellidoPat();
            registros[3] = emp.getApellidoMat();
            registros[4] = emp.getCorreo();
            registros[5] = String.valueOf(emp.getFono());
            registros[6] = "cargo";
            registros[7] = String.valueOf(emp.getSueldo());
            
         
            modelo.addRow(registros);
        }
        jTableEmpleados.setModel(modelo);
       

    }
    
    
        public void populateForm(String rut){

             EmpleadoDao empleadoDao=new EmpleadoDao();
             empleadoDao.openCurrentSession();
             Empleado empleado=empleadoDao.findByRut(rut);
             
             if(empleado!=null){
             jTextFieldRut.setText(empleado.rut);
             jTextFieldNombres.setText(empleado.nombre);
             jTextFieldApellidoPat.setText(empleado.apellidoPat);
             jTextFieldApellidoMat.setText(empleado.apellidoMat);
             jTextFieldCorreo.setText(empleado.correo);
             jTextFieldDireccion.setText(empleado.getDireccion());
             jTextFieldTelefono.setText(empleado.getFono());
             jFSueldo.setText(String.valueOf(empleado.getSueldo()));
             jFHH.setText(String.valueOf(empleado.getHhContratadas()));
             
             }
             empleadoDao.closeCurrentSession();


        }
    
    
