    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
     EmpleadoDao empleadoDao=new EmpleadoDao();
        
     
     empleadoDao.openCurrentSessionwithTransaction();
     
     
      Empleado empleado=empleadoDao.findByRut(jTextFieldRut.getText());
      if(empleado==null){
       empleado=new Empleado();
      }
     
     empleado.setRut(jTextFieldRut.getText());
     empleado.setNombre(jTextFieldNombres.getText());
     empleado.setApellidoPat(jTextFieldApellidoPat.getText());
     empleado.setApellidoMat(jTextFieldApellidoMat.getText());
     empleado.setCorreo(jTextFieldCorreo.getText());
     empleado.setFechaIngreso(new Date());
     empleado.setFechaNacimiento(new Date(jFFechaNacimiento.getText()));
     empleado.setHhContratadas(Integer.parseInt(jFHH.getText()));
     empleado.setDireccion(jTextFieldDireccion.getText());
     empleado.setFono(jTextFieldTelefono.getText());
     empleado.setSueldo(Float.parseFloat(jFSueldo.getText()));
     
     if(empleado.getPersona_id()>0){
      empleadoDao.update(empleado);
     }else{
      empleadoDao.save(empleado);
     }
    
     cargaJtable(empleadoDao.findAll());
     empleadoDao.closeCurrentSessionwithTransaction();
     
// TODO add your handling code here:
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

    
